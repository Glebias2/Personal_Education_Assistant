class TestGenerationPrompts:

    # Легкий уровень сложности
    @staticmethod
    def get_prompt_easy(material: str, num_questions: int) -> str:
        return f"""Ты генератор тестов. На основе материала создай {num_questions} вопросов с 4 вариантами ответов.

Уровень: ЛЕГКИЙ (проверяет запоминание фактов и определений)

Материал:

{material}

ТРЕБОВАНИЯ:

1. Каждый вопрос должен быть четким и проверять знание и запоминание конкретных фактов (год создания чего-либо, имя ученного и т.д.)
2. Правильный ответ должен быть объективно правильным
3. Варианты ответов должны быть похожи по длине
4. Ответ обязательно должен быть с новой строки!!!
5. Используй формат точно так:

ВОПРОС 1: Какой факт/дата/определение?

A) Вариант 1
B) Вариант 2
C) Вариант 3
D) Вариант 4

ОТВЕТ: [A/B/C/D]

ВОПРОС 2: Следующий вопрос?

A) Вариант 1
B) Вариант 2
C) Вариант 3
D) Вариант 4

ОТВЕТ: [A/B/C/D]

Продолжи для остальных {num_questions} вопросов."""

    # Средний уровень сложности
    @staticmethod
    def get_prompt_medium(material: str, num_questions: int) -> str:
        return f"""Ты генератор тестов. На основе материала создай {num_questions} вопросов с 4 вариантами ответов.

Уровень: СРЕДНИЙ (проверяет понимание концепций и связей)

Материал:

{material}

ТРЕБОВАНИЯ:

1. Вопросы должны проверять понимание, а не просто запоминание
2. Используй "почему", "как", "что произойдет если"
3. Правильный ответ должен быть логичен и обоснован материалом
4. Варианты должны быть правдоподобны но неправильны
5. Ответ обязательно должен быть с новой строки!!!
6. Используй формат точно так:

ВОПРОС 1: Почему/Как/Что происходит...?

A) Вариант 1
B) Вариант 2
C) Вариант 3
D) Вариант 4

ОТВЕТ: [A/B/C/D]

Продолжи для остальных {num_questions} вопросов."""

    # Сложный уровень сложности
    @staticmethod
    def get_prompt_hard(material: str, num_questions: int) -> str:
        return f"""Ты генератор тестов. На основе материала создай {num_questions} вопросов с 4 вариантами ответов.

Уровень: СЛОЖНЫЙ (проверяет анализ, синтез и критическое мышление)

Материал:

{material}

ТРЕБОВАНИЯ:

1. Вопросы должны требовать анализа и синтеза информации
2. Используй "сравните", "проанализируйте", "оцените", "выведите"
3. Правильный ответ требует понимания и применения знаний
4. Варианты должны быть похожи и требовать глубокого размышления
5. Ответ обязательно должен быть с новой строки!!!
6. Используй формат точно так:

ВОПРОС 1: Сравните/Проанализируйте/Оцените...?

A) Вариант 1
B) Вариант 2
C) Вариант 3
D) Вариант 4

ОТВЕТ: [A/B/C/D]

Продолжи для остальных {num_questions} вопросов."""

    # Проверяет, является ли реальной учебной темой
    @staticmethod
    def get_topic_validation_prompt(topic: str) -> str:
        return f"""Проверь, является ли "{topic}" реальной учебной темой, концепцией или предметом.

ВАЖНО: Ты должен ответить ТОЛЬКО "YES" или "NO" на английском.

- Напиши "YES" если это реальная тема (например: Python, фотосинтез, история Франции, квантовая механика)
- Напиши "NO" если это вымышленная, неправильная или не существующая тема

Тема: {topic}

Ответ:"""

    # Генерирует подсказку с указанием темы тестирования
    @staticmethod
    def get_prompt_with_topic(difficulty: str, material: str, num_questions: int, topic: str) -> str:
        base_prompt = TestGenerationPrompts.get_prompt(difficulty, material, num_questions)
        return f"{base_prompt}\n\nТЕМА ТЕСТА: {topic}\n\nОБЯЗАТЕЛЬНО: Вопросы должны быть основаны на теме '{topic}' и материале выше."

    # Получает промт по уровню сложности
    @staticmethod
    def get_prompt(difficulty: str, material: str, num_questions: int) -> str:
        if difficulty == 'easy':
            return TestGenerationPrompts.get_prompt_easy(material, num_questions)
        elif difficulty == 'medium':
            return TestGenerationPrompts.get_prompt_medium(material, num_questions)
        elif difficulty == 'hard':
            return TestGenerationPrompts.get_prompt_hard(material, num_questions)
        else:
            return TestGenerationPrompts.get_prompt_medium(material, num_questions)
